import os
import helpers.video_to_audio as vt
import helpers.audio_to_text as at
import helpers.extract_text_from_pdf as ep
import helpers.summarize_text as st
import helpers.save_text_to_pdf as sp

import gradio as gr

def generate_summary_from_video(video_file_path: str):
    audio_path = os.path.join("output", "temp_audio.mp3")
    text = at.audio_to_text(vt.video_to_audio(video_file_path, audio_path))
    summary = st.summarize_text(text)
    pdf_path = os.path.join("output", "video_summary.pdf")
    sp.save_text_to_pdf(summary, pdf_path)
    return summary, pdf_path

def generate_summary_from_audio(audio_path: str):
    text = at.audio_to_text(audio_path)
    summary = st.summarize_text(text)
    pdf_path = os.path.join("output", "audio_summary.pdf")
    sp.save_text_to_pdf(summary, pdf_path)
    return summary, pdf_path

def generate_summary_from_pdf(pdf_path: str):
    text = ep.extract_text_from_pdf(pdf_path)
    summary = st.summarize_text(text)
    pdf_path_sum = os.path.join("output", "summarized.pdf")
    sp.save_text_to_pdf(summary, pdf_path_sum)
    return summary, pdf_path_sum

